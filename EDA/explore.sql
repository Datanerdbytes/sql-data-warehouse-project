-- Explore All Objects in the Database
SELECT *
FROM INFORMATION_SCHEMA.TABLES

-- Explore All Columns in the Database
SELECT *
FROM INFORMATION_SCHEMA.COLUMNS
WHERE TABLE_NAME = 'dim_customers'

-- Explore all countries our customers come from
SELECT DISTINCT 
    country
FROM gold.dim_customers

-- Explore All Categories "The Major Divisions"
SELECT DISTINCT 
    category,
    subcategory,
    product_name
FROM gold.dim_products
ORDER BY 1, 2, 3

-- Find the date of the first and last order
SELECT 
    MIN(order_date) AS first_order_date,
    MAX(order_date) AS last_order_date,
    DATEDIFF(year, MIN(order_date), MAX(order_date))
FROM gold.fact_sales

-- Find the youngest and oldest customer
SELECT 
    MIN(birthdate) AS oldest_birthdate,
    DATEDIFF(year,  MIN(birthdate),GETDATE()) AS oldest_age,
    MAX(birthdate) AS youngest_birthdate,
    DATEDIFF(year,  MAX(birthdate),GETDATE()) AS youngest_age
FROM gold.dim_customers

-- Find the total Sales
SELECT SUM(sales_amt) AS total_sales FROM gold.fact_sales
-- Find how many items are sold
SELECT SUM(quantity) AS total_quantity FROM gold.fact_sales
-- Find the average selling price
SELECT AVG(price) AS avg_price FROM gold.fact_sales
-- Find the total number of Orders
SELECT COUNT(order_number) AS total_orders FROM gold.fact_sales
-- Always compare it using distinct 
SELECT COUNT(DISTINCT order_number) AS total_orders FROM gold.fact_sales
-- Find the total number of products
SELECT COUNT(product_key) AS total_products FROM gold.dim_products
-- Distinct
SELECT COUNT(DISTINCT product_key) AS total_products FROM gold.dim_products
-- Find the total number of customers
SELECT COUNT(customer_key) AS total_products FROM gold.dim_customers
-- Find the total number of customers that has placed an order
SELECT 
    COUNT(DISTINCT customer_key) AS total_customers
FROM gold.fact_sales


-- Generate a Report that shows all key metrics of the business
SELECT 'Total Sales' AS measure_name, SUM(sales_amt) AS measure_value FROM gold.fact_sales
UNION ALL
SELECT 'Total Quantity' AS measure_name, SUM(quantity) AS measure_value FROM gold.fact_sales
UNION ALL 
SELECT 'Avg_Price' AS measure_name, AVG(price) AS measure_value FROM gold.fact_sales
UNION ALL
SELECT 'Total Nr. Orders' AS measure_name, COUNT(DISTINCT order_number) AS measure_value FROM gold.fact_sales
UNION ALL
SELECT 'Total Nr. Products' AS measure_name, COUNT(product_key) AS measure_value FROM gold.dim_products
UNION ALL
SELECT 'Total Nr. Customers' AS measure_name, COUNT(customer_key) AS measure_value FROM gold.dim_customers



