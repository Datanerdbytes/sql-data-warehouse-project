-- Find total products by category
SELECT 
    category,
    COUNT(product_key) AS Total_Products
FROM gold.dim_products
GROUP BY category
ORDER BY Total_Products DESC
-- What is the average costs in each category?
SELECT 
    category,
   AVG(cost) AS Avg_cost
FROM gold.dim_products
GROUP BY category
ORDER BY Avg_cost DESC
-- What is the total revenue generated for each category?
SELECT
    p.category AS Category,
    SUM(s.sales_amt) total_revenue
FROM gold.fact_sales s
LEFT JOIN gold.dim_products p  
ON p.product_key = s.product_key
GROUP BY p.category
ORDER BY total_revenue DESC
-- Find total revenue is generated by each customer
SELECT 
    c.customer_key,
    c.first_name,
    c.last_name,
    SUM(f.sales_amt) total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c  
ON c.customer_key = f.customer_key
GROUP BY 
    c.customer_key,
    c.first_name,
    c.last_name
ORDER BY total_revenue DESC
-- What is the distribution of sold items across countries?

SELECT 
    c.country,
    SUM(f.quantity) total_quantity
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c  
ON c.customer_key = f.customer_key
GROUP BY c.country
ORDER BY total_quantity DESC


-- What are the 5 worst-peforming products in terms of sales?

SELECT TOP 5
    p.product_name,
    SUM(f.sales_amt) total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_products p  
ON p.product_key = f.product_key
GROUP BY p.product_name
ORDER BY total_revenue DESC

SELECT 
    *
FROM (
    SELECT 
        p.product_name,
        SUM(f.sales_amt) total_revenue, 
        ROW_NUMBER () OVER (ORDER BY SUM(f.sales_amt) DESC) AS rank_products
    FROM gold.fact_sales f
    LEFT JOIN gold.dim_products p  
    ON p.product_key = f.product_key
    GROUP BY p.product_name )
t WHERE rank_products <= 5
